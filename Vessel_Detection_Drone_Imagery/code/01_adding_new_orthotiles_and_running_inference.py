from rasterio.enums import Resampling
target_res = 0.10  # 10 cm = 0.10 m/pixel
resampled_tif = "resampled_input.tif"

with rasterio.open(tif_path) as src:
    xres, yres = src.res
    print(f"ðŸ“ Input resolution: {xres:.3f} m/px x {yres:.3f} m/px")

    if abs(xres - target_res) > 1e-3 or abs(yres - target_res) > 1e-3:
        print("âš¡ Resampling to 10 cm/px ...")
        scale_x = xres / target_res
        scale_y = yres / target_res

        new_width = int(src.width * scale_x)
        new_height = int(src.height * scale_y)

        profile = src.profile
        profile.update({
            "width": new_width,
            "height": new_height,
            "transform": src.transform * src.transform.scale(
                src.width / new_width,
                src.height / new_height
            )
        })

        with rasterio.open(resampled_tif, "w", **profile) as dst:
            for b in range(1, src.count + 1):
                data = src.read(
                    b,
                    out_shape=(new_height, new_width),
                    resampling=Resampling.bilinear
                )
                dst.write(data, b)
        tif_path = resampled_tif
        print("âœ… Resampled saved:", resampled_tif)
    else:
        print("âœ… Already 10 cm/px, no resampling needed.")

